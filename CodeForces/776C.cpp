// ============================================================= //
////  hash for ll <>
struct custom_hash_ll {
    static uint64_t splitmix64(uint64_t x) {
        x += 0x9e3779b97f4a7c15;
        x = (x ^ (x >> 30)) * 0xbf58476d1ce4e5b9;
        x = (x ^ (x >> 27)) * 0x94d049bb133111eb;
        return x ^ (x >> 31);
    }

    size_t operator()(uint64_t x) const {
        static const uint64_t FIXED_RANDOM = chrono::steady_clock::now().time_since_epoch().count();
        return splitmix64(x + FIXED_RANDOM);
    }
};

void check(){
	ll n ,a[MAXN] ,pre[MAXN] = {0} ,k;
	cin >> n >> k;

	gp_hash_table<ll ,ll ,custom_hash_ll> mp;
	mp[1] = 1;
	ll pw = k;
	if(k != -1 && k != 1)  for(ll i = 0 ; abs(pw) <= 1e14; i++){ mp[pw] =1 ; pw *= k; }
	else if(k == -1)  mp[-1] = 1;

	for(ll i = 1; i <= n ;i++) {
		cin >> a[i];
		pre[i] = pre[i-1] + a[i];
	}
	
	ll ans = 0;
	
	auto get = [&](ll x)->ll{
		gp_hash_table<ll ,ll ,custom_hash_ll> mp1;
		ll ret = 0;
		mp1[0]++;
		for(ll i = 1 ;i <= n ;i++){
			ret += mp1[pre[i] - x];
			mp1[pre[i]]++;
		}
		return ret;
	};

	for(auto val : mp){ ans += get(val.F); }
	cout << ans<<'\n';
}

